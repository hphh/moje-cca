package cz.cca.mojecca.service.imis.model;

public class Zakazka {
	
	private String zakazka;
	private String popis;
	private String garant;
	
	
	public String getZakazka() {
		return zakazka;
	}
	public void setZakazka(String zakazka) {
		this.zakazka = zakazka;
	}
	public String getPopis() {
		return popis;
	}
	public void setPopis(String popis) {
		this.popis = popis;
	}
	public String getGarant() {
		return garant;
	}
	public void setGarant(String garant) {
		this.garant = garant;
	}
	
	

}
